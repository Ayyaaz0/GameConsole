/// ===== GAME RUNNER =====
static void Run_Selected_Game(int game_index) {
  const GameApi *game = game_list[game_index];
  game->init();
  while (!game->should_exit()) {
    uint32_t frame_start = HAL_GetTick();
    game->update();
    game->render();

    uint32_t frame_time = HAL_GetTick() - frame_start;
    if (frame_time < GAME_RUNNER_FRAME_TIME_MS) {
      HAL_Delay(GAME_RUNNER_FRAME_TIME_MS - frame_time); // Cap at ~60 FPS (16 ms per frame)
    }
  }
  game->shutdown();
}

