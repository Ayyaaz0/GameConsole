/**
 * @brief  The application entry point - initializes hardware, peripherals, and
 * runs the menu loop.
 * @retval int
 */
int main(void) {
  /* MCU Configuration */
  HAL_Init();
  SystemClock_Config();
  PeriphCommonClock_Config();

  /* Initialize peripherals */
  MX_GPIO_Init();
  MX_USART2_UART_Init();
  MX_ADC1_Init(); // Initialize ADC for joystick
  MX_RNG_Init();  // Initialize RNG (if used by games)

  // Initialize LCD first (this sets up GPIOB pins)
  LCD_init(&cfg0);

  // Initialize buzzer timer
  MX_TIM2_Init();
  buzzer_init(&buzzer_cfg);

  // Initialize TIM4 AFTER LCD to avoid GPIO conflict on PB6
  MX_TIM4_Init();

  // Initialize general-purpose timers (available for student use)
  // TIM6: 100ms interrupt timer (10 Hz) - Prescaler=7999, Period=99
  //       Tick frequency = 80MHz / (7999+1) / (99+1) = 100Hz (10ms period)
  // TIM7: 1000ms interrupt timer (1 Hz) - Prescaler=7999, Period=9999
  //       Tick frequency = 80MHz / (7999+1) / (9999+1) = 1Hz (1s period)
  MX_TIM6_Init();
  MX_TIM7_Init();

  // Start TIM6 interrupt so a 10ms periodic tick is always available.
  HAL_TIM_Base_Start_IT(&htim6);

  // TIM7 is available if students want a second slower periodic trigger.
  // HAL_TIM_Base_Start_IT(&htim7);

  // Initialize Input System (button handling)
  Input_Init();

  // Initialize Joystick
  Joystick_Init(&joystick_cfg);

  // Clear screen
  LCD_Fill_Buffer(0);
  LCD_Refresh(&cfg0);

  // Startup animation
  LCD_printString("MENU SYSTEM", 40, 50, 1, 2);
  LCD_printString("Starting...", 50, 100, 1, 2);
  LCD_Refresh(&cfg0);
  HAL_Delay(1000);

  // Initialize PWM for LED control
  PWM_Init(&pwm_cfg);
  PWM_SetFreq(&pwm_cfg, 1000);
  PWM_SetDuty(&pwm_cfg, 50); // Start at 50% brightness

  // Ensure LD2 on PA5 starts OFF
  HAL_GPIO_WritePin(GPIOA, GPIO_PIN_5, GPIO_PIN_RESET);

  Menu_Init(&menu);

  printf("Menu system initialized. Press BT3 to select.\n");

  while (1) {
    int selected_game = Menu_Run(&menu);
    Run_Selected_Game(selected_game);
  }
  return 0;
}

